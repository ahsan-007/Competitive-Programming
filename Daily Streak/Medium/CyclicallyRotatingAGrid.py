from typing import List


class Solution:
    def rotateGrid(self, grid: List[List[int]], k: int) -> List[List[int]]:
        def rototaeUtil(grid, i, j, layer):
            if i >= len(grid) - layer or j >= len(grid[i]) - layer:
                return grid

            r = len(grid) - layer
            c = len(grid[i]) - layer

            t = grid[i][j]

            for l in range(j, c-1):
                grid[i][l] = grid[i][l+1]

            for l in range(i, r-1):
                grid[l][c-1] = grid[l+1][c-1]

            for l in range(c-1, j, -1):
                grid[r-1][l] = grid[r-1][l-1]

            for l in range(r-1, i, -1):
                grid[l][j] = grid[l-1][j]

            if i + 1 < r:
                grid[i+1][j] = t

            return grid

        n = len(grid)
        m = len(grid[0])
        mod = 0
        layer = 0
        while m > 0 and n > 0:
            mod = ((m + n) * 2) - 4
            m = m - 2
            n = n - 2
            for i in range(k % mod):
                rototaeUtil(grid, layer, layer, layer)
            layer += 1
        return grid


